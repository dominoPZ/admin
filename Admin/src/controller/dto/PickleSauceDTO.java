package controller.dto;

public class PickleSauceDTO {

	//맴버변수
	private String dr_no;
	private String d_name;
	private String d_price;
	private String d_img;
	private String d_kind;
	//GetSet
	
	public String getD_name() {
		return d_name;
	}
	public void setD_name(String d_name) {
		this.d_name = d_name;
	}
	public String getD_price() {
		return d_price;
	}
	public void setD_price(String d_price) {
		this.d_price = d_price;
	}
	public String getD_img() {
		return d_img;
	}
	public void setD_img(String d_img) {
		this.d_img = d_img;
	}
	public String getDr_no() {
		return dr_no;
	}
	public void setDr_no(String dr_no) {
		this.dr_no = dr_no;
	}
	public String getD_kind() {
		return d_kind;
	}
	public void setD_kind(String d_kind) {
		this.d_kind = d_kind;
	}
	
}
